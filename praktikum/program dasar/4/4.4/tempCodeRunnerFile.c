#include <stdio.h>


int main()
{
    int input,i,hasil=0,j,tambah=-1;
    printf("mau berapa kali pengulangan :");
    scanf("%d",&input);
    for (i = 0; i <= input; i++)
    {
        for (j = 1; j == 1; j++)
        {
            tambah++;
        }
        hasil=hasil+tambah;
        printf(" %d",hasil);
    }
   
    return 0;
}